package com.example.pizzarecipe;

public class PizzaRecipe {

    private int image;
    private String name;
    private String ingredients;
    private String description;

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

    public String getIngredients() {
        return ingredients;
    }

    public String getDescription() {
        return description;
    }
}
